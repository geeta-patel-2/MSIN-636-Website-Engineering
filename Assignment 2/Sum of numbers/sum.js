// Define a function to return the sum of two numbers
function sumOfTwoNumbers(num1, num2) {
    return num1 + num2;
}

// Example usage of the sum function
const number1 = 5;
const number2 = 10;

// Call the function with two numbers and log the result
console.log("Sum of", number1, "and", number2, "is:", sumOfTwoNumbers(number1, number2));
